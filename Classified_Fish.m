% --- Executes on button press in pushbutton2.
function pushbutton2_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton2 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

we = str2double(get(handles.we,'String'));
l1 = str2double(get(handles.we,'String'));
l2 = str2double(get(handles.we,'String'));
l3 = str2double(get(handles.we,'String'));
he = str2double(get(handles.we,'String'));
wi = str2double(get(handles.we,'String'));

sample = [we l1 l2 l3 he wi];

opts = detectImportOptions('Fish.csv');
opts.SelectedVariableNames = (2:7);
training = readmatrix('Fish.csv', opts);

opts = detectImportOptions('Fish.csv');
opts.SelectedVariableNames = (1);
group = readmatrix('Fish.csv', opts);

class = fitcknn(training, group, 'NumNeighbors', 3);
Classified = predict(class, sample);

set(handles.result,'String', Classified);
